public class Reltorio {
    

    
    private String codParc;
   private String datavenc;
   private String CodContr;
   private String valor;
   private String data_pag;
   private String vmulta;
   private String vdesconto;
   private String totalpag;
   private String num_parcela;

    /**
     * @return the codParc
     */
    public String getCodParc() {
        return codParc;
    }

    /**
     * @param codParc the codParc to set
     */
    public void setCodParc(String codParc) {
        this.codParc = codParc;
    }

    /**
     * @return the datavenc
     */
    public String getDatavenc() {
        return datavenc;
    }

    /**
     * @param datavenc the datavenc to set
     */
    public void setDatavenc(String datavenc) {
        this.datavenc = datavenc;
    }

    /**
     * @return the CodContr
     */
    public String getCodContr() {
        return CodContr;
    }

    /**
     * @param CodContr the CodContr to set
     */
    public void setCodContr(String CodContr) {
        this.CodContr = CodContr;
    }

    /**
     * @return the valor
     */
    public String getValor() {
        return valor;
    }

    /**
     * @param valor the valor to set
     */
    public void setValor(String valor) {
        this.valor = valor;
    }

    /**
     * @return the data_pag
     */
    public String getData_pag() {
        return data_pag;
    }

    /**
     * @param data_pag the data_pag to set
     */
    public void setData_pag(String data_pag) {
        this.data_pag = data_pag;
    }

    /**
     * @return the vmulta
     */
    public String getVmulta() {
        return vmulta;
    }

    /**
     * @param vmulta the vmulta to set
     */
    public void setVmulta(String vmulta) {
        this.vmulta = vmulta;
    }

    /**
     * @return the vdesconto
     */
    public String getVdesconto() {
        return vdesconto;
    }

    /**
     * @param vdesconto the vdesconto to set
     */
    public void setVdesconto(String vdesconto) {
        this.vdesconto = vdesconto;
    }

    /**
     * @return the totalpag
     */
    public String getTotalpag() {
        return totalpag;
    }

    /**
     * @param totalpag the totalpag to set
     */
    public void setTotalpag(String totalpag) {
        this.totalpag = totalpag;
    }

    /**
     * @return the num_parcela
     */
    public String getNum_parcela() {
        return num_parcela;
    }

    /**
     * @param num_parcela the num_parcela to set
     */
    public void setNum_parcela(String num_parcela) {
        this.num_parcela = num_parcela;
    }
   
    
}
